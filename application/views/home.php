<h2 class="text-center">Anglo-Dutch Wars</h2>

<div class="row">
	<div class="col-md-8 col-md-offset-2">

		<p>The Anglo-Dutch Wars (Dutch: Engels-Nederlandse Oorlogen or Engelse Zeeoorlogen) were a series of wars fought between the English (later British) and the Dutch in the 17th and 18th centuries for control over the seas and trade routes.</p>
		<p>The second and third Anglo-Dutch wars confirmed the Dutch Republic's position as the leading maritime state of the seventeenth century.</p>

		<hr />
		<p>This website tells the story of the naval battles of the Anglo-Dutch Wars with artwork from that era. Each battle is first depicted on the map and it's up to you to pick a side, English or Dutch. For every correct guess, you earn a point for your total score.</p>

